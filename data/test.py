    with open('norms.json', 'w', encoding='utf-8') as f:
        json.dump(norms, f, ensure_ascii=False, indent=2)
    
    with open('connections.json', 'w', encoding='utf-8') as f:
        json.dump(connections, f, ensure_ascii=False, indent=2)
    
